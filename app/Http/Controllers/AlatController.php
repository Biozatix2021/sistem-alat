    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // return $request->file('foto')->getClientOriginalName();
        $pathname = $request->file('foto')->getClientOriginalName();

        Alat::Create(
            [
                'nama_alat' => $request->nama_alat,
                'brand' => $request->brand,
                'kategori' => $request->type_alat,
                'spesifikasi' => $request->spesifikasi,
                'foto' => $pathname
            ]
        );

        if ($request->hasFile('foto')) {
            $request->file('foto')->move('product/images/', $pathname);
        }
        return response()->json(['success' => 'Alat berhasil disimpan.']);
    }

